# Tipos int e float

# int = números inteiros
# Representa qualquer número inteiro
# sendo positivo ou negativo

# Exemplo:
print(12)
print(-12)
print(0)

# float = números com ponto flutuante
# Representa qualquer número com ponto flutuante 
# sendo positivo ou negativo

#Exemplo:

print(11.12, 13.14)
print(0.0, -12.12)

# A função type mostra o tipo que o Python inferiu ao valor

print(type("hello world!"))
print(type(12))
print(type(12.12))



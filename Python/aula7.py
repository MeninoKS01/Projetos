# Variáveis são usadas para salvar uma informação na memória do computador
# iniciando uma variável sempre com letra minúscula, pode ser usado números
# e sem espaços, apenas com "_".
# O sinal de "=" vai ser usado para atribuir um valor à uma variável
# nome_variável = expressão

# Lembre que o nome da variável deve ser sempre condizente 
# com o valor atribuído!

#Exemplo:

nome_completo = "Nathan Olivares da Silva"
idade = 18
maior_de_idade = idade >= 18

print("Nome completo:", nome_completo)
print("Idade:", idade)
print("É maior de idade?", maior_de_idade)
# Operadores aritméricos

adicao = 10 + 10
print("Adição:", adicao)

subtracao = 10 - 5
print("Subtração:", subtracao)

multiplicacao = 10 * 10
print("Multiplicação:", multiplicacao)

divisao = 10 / 3 #float
print("Divisão:", divisao) 

divisao_inteira = 10 // 3 #int
print("Divisão inteira:", divisao_inteira) 

exponenciacao = 2 ** 10
print("Exponenciação:", exponenciacao)

modulo = 25 % 2 #resto da divisão
print("Módulo:", modulo)

print(12 % 5 == 0)
print(12 % 6 == 0)